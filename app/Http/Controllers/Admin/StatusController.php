    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy(Status $status)
    {
        $status->find($status->id)->all();

        $status->delete();

        if($status){
            return redirect()->route('status.index')->with(['success' => 'Berhasil Delete Data']);
        } else {
            return redirect()->route('status.index')->with(['error' => 'Gagal Delete Data']);
        }
    }
